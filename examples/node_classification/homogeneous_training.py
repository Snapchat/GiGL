"""
This file contains an example for how to run homogeneous supervised node classification (SNC)
training using live subgraph sampling powered by GraphLearn-for-PyTorch (GLT). While
`_run_example_training` is coupled with GiGL orchestration, the `_training_process` and
`_run_validation_loops` functions are generic and can be used as references for writing training
for pipelines not dependent on GiGL orchestration.

To run this file with GiGL orchestration, set the fields similar to below:

trainerConfig:
  trainerArgs:
    # Example argument to trainer
    log_every_n_batch: "25"
  command: python -m examples.node_classification.homogeneous_training
featureFlags:
  should_run_glt_backend: 'True'

You can run this example in a full pipeline with `make run_hom_cora_snc_e2e_test`
from GiGL root.

Given a frozen task config with already-populated data preprocessor output, the following training
script can be run locally using:
WORLD_SIZE=1 RANK=0 MASTER_ADDR="localhost" MASTER_PORT=20000 \
    python -m examples.node_classification.homogeneous_training \
    --task_config_uri=<frozen_task_config_uri>

A frozen task config with data preprocessor outputs can be generated by running an e2e pipeline
with `stop_after=data_preprocessor` and using the frozen config generated from the
`config_populator` component after the run has completed.
"""

